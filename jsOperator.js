//logical operators-return boolean value

//&&
var a=20;
var b=10;
document.write((a>b)&&(b==10)+"<br>");
document.write((a>b)&&(b!=10)+"<br>");

// ||
var a=20;
var b=10;
document.write((a>b)||(b==10)+'<br>');
document.write((a>b)||(b!=10)+'<br>');

// !
var a=20;
var b=10;
document.write(!(a>b)+'<br>');

